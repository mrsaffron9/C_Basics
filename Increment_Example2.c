#include <stdio.h>
int main() {
    float lone;
    lone=501;
    printf("lone = %.1f\n",lone);
    lone+=99;
    printf("lone = %.1f\n",lone);
    lone-=250;
    printf("lone = %.1f\n",lone);
    lone/=82;
    printf("lone = %.1f\n",lone);
    lone*=4.3;
    printf("lone = %.1f\n",lone);
    return 0;
}

